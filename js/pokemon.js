        let bonus = '';
        if(Math.random() < 0.5){ player.addItem(I.GREATBALL, 1); bonus = ' + 슈퍼볼'; }
        if(Math.random() < 0.25){ player.addItem(I.RARECANDY, 1); bonus += ' + 이상한사탕'; }
