    if(this.active || game.inBattle) return;
    if(!PokeMan.partyAlive()){ UI.toast('내 포켓몬이 모두 지쳐 있다...'); return; }
    this._pvpPendingTo = id;
    Net.sendTo(id, { t: 'pvpReq', name: Net.myName || '친구', party: PokeMan.party.map(q => q.serialize()) });
