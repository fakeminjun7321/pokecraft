function buildPokeModel(spId, shiny){
  const sp = SPECIES[spId], M = sp.model;
  let m;
  switch(M.form){
    case 'quad':    m = buildQuad(Object.assign({}, M.o)); break;
    case 'biped':   m = buildBiped(Object.assign({}, M.o)); break;
    case 'bird':    m = buildBird(Object.assign({}, M.o)); break;
    case 'blob':    m = buildBlob(Object.assign({}, M.o)); break;
    case 'float':   m = buildFloat(Object.assign({}, M.o)); break;
    case 'serpent': m = buildSerpent(Object.assign({}, M.o)); break;
  }
  if(M.deco) M.deco(m);
  if(shiny){
    // 색이 다른 포켓몬: 색상환을 돌려 전체 색 변형 (재질은 박스마다 새로 생성되므로 안전)
    m.group.traverse(o => {
      if(o.isMesh && o.material && o.material.color && !o.material.userData.shared){
        const hsl = {};
        o.material.color.getHSL(hsl);
        o.material.color.setHSL((hsl.h + 0.45) % 1, clamp(hsl.s * 1.15 + 0.08, 0, 1), hsl.l);
      }
    });
  }
  const s = M.s || 1;
  m.group.scale.setScalar(s);
  const root = new THREE.Group();
  root.add(m.group);
  m.root = root;
  m.scaleVal = s;
  return m;
}

