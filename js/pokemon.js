function baseFormOf(sp){
  if(!_PRE_EVO){
    _PRE_EVO = {};
    for(let i = 1; i < SPECIES.length; i++){
      const s = SPECIES[i];
      if(s && s.evo && s.evo.to) _PRE_EVO[s.evo.to] = i;
    }
    for(const sid in STONE_EVOS) for(const from in STONE_EVOS[sid]) _PRE_EVO[STONE_EVOS[sid][from]] = +from;
  }
  let cur = sp, guard = 0;
  while(_PRE_EVO[cur] && guard++ < 6) cur = _PRE_EVO[cur];
  return cur;
}
// 진화 체인에서 몇 단계인지 (1 = 베이스)
function evoStage(sp){
  baseFormOf(sp); // _PRE_EVO 빌드 보장
  let depth = 1, cur = sp, guard = 0;
  while(_PRE_EVO[cur] && guard++ < 6){ cur = _PRE_EVO[cur]; depth++; }
  return depth;
}
// 통일된 진화 레벨: 1→2단계 = Lv.30, 2→3단계 = Lv.60 (진화는 어렵게!)
function evoReqLevel(sp){ return evoStage(sp) <= 1 ? 30 : 60; }
